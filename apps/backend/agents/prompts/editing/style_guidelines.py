style_guidelines = """
1. Typography
	•	Max 2 fonts: One for headings, one for body text.
	•	**RECOMMENDED FONT SIZES:**
		- Slide-wide Impact Text: 250-300pt (for 1-2 words across slide)
		- Hero Titles: 80-120pt (main slide titles)
		- Subtitles/Headers: 70-90pt (section headings)
		- Body text/Bullets: 30-36pt (main content)
		- Captions/Labels/Sources: 24-30pt (smallest text)
		- **24pt is the absolute minimum for any text**

CREATIVE FONT GUIDELINES:
• BE ADVENTUROUS: Choose fonts with personality that match your content
• FONT PAIRING: Combine contrasting fonts (serif + sans-serif, bold + light)
• SLIDE-SPECIFIC FONTS: Different slides can use different font combinations
• EXPLORE CATEGORIES:
  - Title slides: Bold/Display fonts (Bebas Neue, Montserrat, Playfair Display)
  - Data slides: Modern/Technical fonts (Inter, Space Grotesk, JetBrains Mono)
  - Content slides: Readable with character (Outfit, Work Sans, Plus Jakarta Sans)
  - Creative slides: Unique/Contemporary fonts (Bricolage Grotesque, Clash Display)
• WEIGHT VARIETY: Use different weights (light, regular, bold) for hierarchy
• AVOID DEFAULTS: Skip Arial, Helvetica, Times New Roman unless specifically requested
• **READABILITY FIRST**: Large, clear text is preferred, but dense content may require smaller sizes

⸻

2. Color
	•	Stick to 2–3 brand colors.
	•	High contrast between text and background (e.g., dark text on light).
	•	Use color sparingly for emphasis (e.g., CTA buttons, highlights).
	•	Avoid neon or oversaturated colors.

⸻

3. Images & Icons
	•	High-resolution only.
	•	Use consistent style (e.g., all photos or all illustrations).
	•	Avoid excessive overlays or filters.
	•	Icons: Flat, monochrome or duotone preferred; use from the same set.

⸻

4. Whitespace
	•	Generous margins around all content.
	•	Avoid edge crowding — no elements closer than ~30 px to edges.
	•	Don't fill space just to fill it.

⸻

5. Animations & Transitions
	•	Minimal and purposeful only.
	•	Avoid distracting transitions (no fly-ins, spins, or sound effects).
	•	Use appear/fade to reveal steps if needed.

⸻

6. Consistency
	•	Slide masters/templates: Use throughout.
	•	Same header/footer positioning on all slides.
	•	Align elements across slides (logos, titles, content blocks).

⸻

7. Text
	•	One idea per slide.
	•	Bullet limit: 3–5 max, or better yet, no bullets at all.
	•	Avoid full sentences; write for scanning, not reading.
"""